const dayOfWeek = ['mon', 'tue', 'wed', 'thu', 'fri', 'sat'];

console.log(dayOfWeek[4]); // fri

// Get Item from Array
console.log(dayOfWeek); // (6) ['mon', 'tue', 'wed', 'thu', 'fri', 'sat']

// Add one more day to the array
dayOfWeek.push('sun');

console.log(dayOfWeek); // (7) ['mon', 'tue', 'wed', 'thu', 'fri', 'sat', 'sun']
